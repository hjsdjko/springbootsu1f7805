<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				fuzerenzhanghao : false,
				fuzerenxingming : false,
				shetuanbianhao : false,
				shetuanmingcheng : false,
				shetuanleixing : false,
				tupian : false,
				shetuanrenshu : false,
				shenqingshijian : false,
				shenqingshuoming : false,
				yonghuzhanghao : false,
				yonghuxingming : false,
				sfsh : false,
				shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          fuzerenzhanghao: '',
          fuzerenxingming: '',
          shetuanbianhao: '',
          shetuanmingcheng: '',
          shetuanleixing: '',
          tupian: '',
          shetuanrenshu: Number('1') ,
          shenqingshijian: '',
          shenqingshuoming: '',
          yonghuzhanghao: '',
          yonghuxingming: '',
        },
        shetuanrenshuOptions: [],


        rules: {
          fuzerenzhanghao: [
          ],
          fuzerenxingming: [
          ],
          shetuanbianhao: [
          ],
          shetuanmingcheng: [
          ],
          shetuanleixing: [
          ],
          tupian: [
          ],
          shetuanrenshu: [
            { required: true, message: '申请人数不能为空', trigger: 'blur' },
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          shenqingshijian: [
          ],
          shenqingshuoming: [
          ],
          yonghuzhanghao: [
          ],
          yonghuxingming: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.shenqingshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='fuzerenzhanghao'){
              this.ruleForm.fuzerenzhanghao = obj[o];
              this.ro.fuzerenzhanghao = true;
              continue;
            }
            if(o=='fuzerenxingming'){
              this.ruleForm.fuzerenxingming = obj[o];
              this.ro.fuzerenxingming = true;
              continue;
            }
            if(o=='shetuanbianhao'){
              this.ruleForm.shetuanbianhao = obj[o];
              this.ro.shetuanbianhao = true;
              continue;
            }
            if(o=='shetuanmingcheng'){
              this.ruleForm.shetuanmingcheng = obj[o];
              this.ro.shetuanmingcheng = true;
              continue;
            }
            if(o=='shetuanleixing'){
              this.ruleForm.shetuanleixing = obj[o];
              this.ro.shetuanleixing = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='shetuanrenshu'){
              this.ruleForm.shetuanrenshu = obj[o];
              this.ro.shetuanrenshu = true;
              continue;
            }
            if(o=='shenqingshijian'){
              this.ruleForm.shenqingshijian = obj[o];
              this.ro.shenqingshijian = true;
              continue;
            }
            if(o=='shenqingshuoming'){
              this.ruleForm.shenqingshuoming = obj[o];
              this.ro.shenqingshuoming = true;
              continue;
            }
            if(o=='yonghuzhanghao'){
              this.ruleForm.yonghuzhanghao = obj[o];
              this.ro.yonghuzhanghao = true;
              continue;
            }
            if(o=='yonghuxingming'){
              this.ruleForm.yonghuxingming = obj[o];
              this.ro.yonghuxingming = true;
              continue;
            }
          }
            this.ruleForm.shetuanrenshu=Number('1') ;
            this.ruleForm.shetuanrenshu = 0;
            this.ro.shetuanrenshu = false;
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0){
                this.ruleForm.yonghuzhanghao = json.yonghuzhanghao;
				this.ro.yonghuzhanghao = true;
            }
            if((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0){
                this.ruleForm.yonghuxingming = json.yonghuxingming;
				this.ro.yonghuxingming = true;
            }
          }
        });
        this.shetuanrenshuOptions = "1".split(',')

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`shenqingshetuan/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {
			if(!this.ruleForm.shetuanrenshu){
				this.$message.error("申请人数不能为空");
				return
			}
			var obj = JSON.parse(localStorage.getItem('crossObj'));
			var table = localStorage.getItem('crossTable');
			obj.shetuanrenshu = parseFloat(obj.shetuanrenshu) + parseFloat(this.ruleForm.shetuanrenshu)

			//this.$http.post(table+`/update`, obj).then(res => {});
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('shenqingshetuan/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算
								var obj = JSON.parse(localStorage.getItem('crossObj'));
								var table = localStorage.getItem('crossTable');

								obj.shetuanrenshu = parseFloat(obj.shetuanrenshu) + parseFloat(this.ruleForm.shetuanrenshu)

								this.$http.post(table+`/update`,obj).then(res => {});
								this.$http.post(`shenqingshetuan/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {
						var obj = JSON.parse(localStorage.getItem('crossObj'));
						var table = localStorage.getItem('crossTable');

						obj.shetuanrenshu = parseFloat(obj.shetuanrenshu) + parseFloat(this.ruleForm.shetuanrenshu)

						this.$http.post(table+`/update`,obj).then(res => {});
						this.$http.post(`shenqingshetuan/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      tupianUploadChange(fileUrls) {
          this.ruleForm.tupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");
      },
    }
  };
</script>

